class J03_01
{
	public static void main (String args[])
	{
		int Counter = 0;
		System.out.println("Counter is: "+ Counter);
		System.out.println("\n++ Counter is:"+ ++Counter);
		System.out.println("\n Counter is:" + Counter);
		Counter = 0;
		System.out.println("\n\n Counter is :" + Counter);
		System.out.println("\n Counter ++ is :" + Counter++);
		System.out.println("\n Counter is :" + Counter);
	}
}
